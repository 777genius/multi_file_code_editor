/// Elevation system - consistent shadow depths throughout the app.
class AppElevation {
  AppElevation._();

  /// No elevation - flat on surface
  static const none = 0.0;

  /// Extra small elevation (1dp)
  static const xs = 1.0;

  /// Small elevation (2dp)
  static const s = 2.0;

  /// Medium elevation (4dp)
  static const m = 4.0;

  /// Large elevation (8dp)
  static const l = 8.0;

  /// Extra large elevation (12dp)
  static const xl = 12.0;

  /// Extra extra large elevation (16dp)
  static const xxl = 16.0;

  /// Maximum elevation (24dp)
  static const max = 24.0;
}
