import '../tokens/primitive/size_primitives.dart';

/// Icon size system - consistent icon sizing throughout the app.
class AppIconSize {
  AppIconSize._();

  /// Extra small icon (16px)
  static const xs = SizePrimitives.size16;

  /// Small icon (20px)
  static const s = SizePrimitives.size20;

  /// Medium icon (24px) - default
  static const m = SizePrimitives.size24;

  /// Large icon (32px)
  static const l = SizePrimitives.size32;

  /// Extra large icon (48px)
  static const xl = SizePrimitives.size48;

  /// Extra extra large icon (64px)
  static const xxl = SizePrimitives.size64;
}
